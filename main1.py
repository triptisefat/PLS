# Primitive data types demonstration

# Integer
age = 21
print("Age (int):", age)

# Floating Point
gpa = 3.75
print("GPA (float):", gpa)

# Character (Python uses string for single characters too)
initial = 'S'
print("Initial (char):", initial)

# Boolean
is_graduate = True
print("Is Graduate (bool):", is_graduate)

# String (though not primitive in some languages)
name = "Sefat"
university = "IUBAT"
print("Name (string):", name)
print("University (string):", university)
